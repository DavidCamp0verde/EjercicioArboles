    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Integer i = JOptionPane.showConfirmDialog(this, "Â¿Esta seguro de que quiere eliminar la posicion?");
        if (i == JOptionPane.OK_OPTION) {

            try {
                arbol.getPosicion().eliminar(tblPosiciones.getSelectedRow());
                pc.getPosiciones().eliminar(tblPosiciones.getSelectedRow());
            } catch (Exception e) {
            }
            Utilidades.guardarPosiciones(pc);
            Utilidades.guardarArchivoJSON(ac);
            cargarTabla();

        }


    }//GEN-LAST:event_btnEliminarActionPerformed

