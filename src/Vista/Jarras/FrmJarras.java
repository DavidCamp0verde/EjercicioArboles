    private void ejecutar(){
        Integer eInicialJG = cbxEInicialJG.getSelectedIndex();
        Integer eFinalJG = cbxEFinalJG.getSelectedIndex();
        Integer eInicialJP = cbxEInicialJP.getSelectedIndex();
        Integer eFinalJP = cbxEFinalJP.getSelectedIndex();
        nodoEInicial.fijarEstadoJarras(eInicialJG, eInicialJP);
        nodoEFinal.fijarEstadoJarras(eFinalJG, eFinalJP);
        arbolJarras = new ArbolJarras(nodoEInicial, nodoEFinal);
    }

